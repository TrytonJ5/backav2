class Palestra:

    def __init__(self, id, nome, descricao, dt_palestra, horario_inicio_palestra, horario_fim_palestra, nome_palestrante, minicurriculo_palestrante, id_evento):
        self.id = id
        self.nome = nome
        self.descricao = descricao
        self.dt_palestra = dt_palestra
        self.horario_inicio_palestra = horario_inicio_palestra
        self.horario_fim_palestra = horario_fim_palestra
        self.nome_palestrante = nome_palestrante
        self.minicurriculo_palestrante = minicurriculo_palestrante
        self.id_evento = id_evento
